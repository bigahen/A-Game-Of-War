    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
    	int num;
    	Card p1Card, p2Card;
		num = r.nextInt(list.size());
		p1Card = new Card(list.get(num).getSuit(), list.get(num).getValue());
		list.remove(num);
		
		num = r.nextInt(list.size());
		p2Card = new Card(list.get(num).getSuit(), list.get(num).getValue());
		list.remove(num);
		
		if(p1Card.compareTo(p2Card)>0){
			p1Current++;
		}else if(p1Card.compareTo(p2Card)<0){
			p2Current++;
		}
    	try {
			updateImages(p1Card, p2Card);
		} catch (IOException e) {
			e.printStackTrace();
		}
    	
    	if(list.size() == 0){
    		if(p1Current > p2Current){
    			p1Total++;
    		}else if(p2Current > p1Current){
    			p2Total++;
    		}
    		jButton1.setEnabled(false);
    	}
       	updateScore();
    	
    }

    
