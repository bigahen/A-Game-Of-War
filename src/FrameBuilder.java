    public static void updateImages(Card p1Card, Card p2Card) throws IOException{
  	
 		BufferedImage img = null;
			try {
				img = ImageIO.read(FrameBuilder.class.getResource("/cards.png"));
			} catch (IOException e) {
				e.printStackTrace();
			}
			
			final int width = 73;
			final int height = 98;
			final int rows = 4;
			final int cols = 13;
			BufferedImage[] sprites = new BufferedImage[rows * cols];

			for (int i = 0; i < rows; i++)
			{
			    for (int j = 0; j < cols; j++)
			    {
			        sprites[(i * cols) + j] = img.getSubimage(
			            j * width,
			            i * height,
			            width,
			            height
			        );
			    }
			}
			
			int index1 = 0, index2 = 0;
			
			AffineTransform at = new AffineTransform();
			at.scale(2.05, 2.05);
			AffineTransformOp scaleOp = new AffineTransformOp(at, AffineTransformOp.TYPE_BILINEAR);
			
			if(p1Card.getSuit().equals("Spade")){
				index1+=13;
			}else if(p1Card.getSuit().equals("Heart")){
				index1+=26;
			}else if(p1Card.getSuit().equals("Diamond")){
				index1+=39;
			}
			
			if(p1Card.getRank() < 13){
				index1+=p1Card.getRank();
			}	
			
			
			if(p2Card.getSuit().equals("Spade")){
				index2+=13;
			}else if(p2Card.getSuit().equals("Heart")){
				index2+=26;
			}else if(p2Card.getSuit().equals("Diamond")){
				index2+=39;
			}
			
			if(p2Card.getRank() < 13){
				index2+=p2Card.getRank();
			}
			
			BufferedImage after1 = new BufferedImage(150, 206, BufferedImage.TYPE_INT_ARGB);
			after1 = scaleOp.filter(sprites[index1], after1);	
			
			BufferedImage after2 = new BufferedImage(150, 206, BufferedImage.TYPE_INT_ARGB);
			after2 = scaleOp.filter(sprites[index2], after2);	
			
		if(p1Card.compareTo(p2Card)>0){
			panel1Label.setIcon((new ImageIcon(colorImage(after1, 50, 0, 50))));
			panel2Label.setIcon((new ImageIcon(colorImage(after2, 0, 50, 50))));
		}else if(p1Card.compareTo(p2Card) < 0){
			panel1Label.setIcon((new ImageIcon(colorImage(after1, 0, 50, 50))));
			panel2Label.setIcon((new ImageIcon(colorImage(after2, 50, 0, 50))));
		}else{
			panel1Label.setIcon((new ImageIcon(colorImage(after1, 0, 0, 0))));
			panel2Label.setIcon((new ImageIcon(colorImage(after2, 0, 0, 0))));
		}
		

	}
 
    public static BufferedImage colorImage(BufferedImage loadImg, int red, int green, int blue) {
	    BufferedImage img = new BufferedImage(loadImg.getWidth(), loadImg.getHeight(), BufferedImage.OPAQUE);
	    Graphics2D graphics = img.createGraphics(); 
	    Color newColor = new Color(red, green, blue, 0);
	    graphics.setXORMode(newColor);
	    graphics.drawImage(loadImg, null, 0, 0);
	    graphics.dispose();
	    return img;
	}
      
